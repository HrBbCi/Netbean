    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int select = tbTTDiemThi.getSelectedRow();
        if (select == -1) {
            JOptionPane.showMessageDialog(this,
                    "Vui lòng chọn điểm muốn sửa");
            return;
        }
        String maSV = (String) tbTTDiemThi.getValueAt(select, 0);
        String maK = (String) tbTTDiemThi.getValueAt(select, 1);
        System.out.println("Mã SV " + maSV+maK);
        frmCapNhatBangDiem frm = new frmCapNhatBangDiem();
        frm.setMaSV(maSV);
        frm.setMaK(maK);
        frm.setVisible(true);
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int select = tbTTDiemThi.getSelectedRow();
        if (select == -1) {
            JOptionPane.showMessageDialog(this,
                    "Vui lòng chọn điểm muốn xóa");
            return;
        }
        BangDiemBussiness bdb = new BangDiemBussiness();
        String maSV = (String) tbTTDiemThi.getValueAt(select, 0);
        String maK = (String) tbTTDiemThi.getValueAt(select, 1);
        if (JOptionPane.showConfirmDialog(this, "Bạn muốn xóa", "Bạn chắc chắn "
                + "muốn xóa", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            boolean kq = bdb.xoaDiem(maSV,maK);
            if (kq) {
                JOptionPane.showMessageDialog(rootPane, "Thành công");
                hienThiDanhSachDiem();
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

