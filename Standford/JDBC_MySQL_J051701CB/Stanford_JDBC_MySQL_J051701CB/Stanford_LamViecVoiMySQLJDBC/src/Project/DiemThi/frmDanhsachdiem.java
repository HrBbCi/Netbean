    public static void hienThiDanhSachDiem(List<DiemThi> lstBangDiem) {
        String colTieuDe[] = new String[]{"Mã SV", "Mã Môn Học", "Họ Tên", "Điểm Thi",
            "Môn Học", "Phòng học", "Ngày Thi"};

        DefaultTableModel model = new DefaultTableModel(colTieuDe, 0);
        Object objSinhVien[];
        DateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        for (DiemThi bd : lstBangDiem) {
            objSinhVien = new Object[colTieuDe.length];
            objSinhVien[0] = bd.getSinhVienID().getMaSV();
            objSinhVien[1] = bd.getMonHocID().getMaMH();
            objSinhVien[2] = bd.getSinhVienID().getHoTen();

            objSinhVien[3] = bd.getDiemThi();
            objSinhVien[4] = bd.getMonHocID().getTenMonHoc();
            objSinhVien[5] = bd.getPhongHocID().getTenPhong();
            if (bd.getNgayThi() != null) {
                objSinhVien[6] = df.format(bd.getNgayThi());
            }
            //Thêm vào model
            model.addRow(objSinhVien);
        }
        System.out.println("lstBangDiem : " + lstBangDiem.size());
        //Hiển thị thông tin lên JTable 
        tbTTDiemThi.setModel(model);

    }

