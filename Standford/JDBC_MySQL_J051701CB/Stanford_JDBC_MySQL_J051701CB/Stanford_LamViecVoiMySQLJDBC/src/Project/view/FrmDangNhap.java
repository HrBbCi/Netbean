    private void btnDangNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangNhapActionPerformed
        String username = txtDangNhap.getText();
        char[] arr = txtMatKhau.getPassword();
        String password = new String(arr);

        // Tạo thông tin user
        Users user = new Users();
        user.setUsername(username);
        user.setPassword(password);

        // Kiểm tra đăng nhập trong DB
        UsersBussiness bussiness = new UsersBussiness();
        boolean isLoginSuccess = bussiness.isHasUser(user);

        if (isLoginSuccess) {
            // Đăng nhập thành công: ẩn form đăng nhập, enable menu trên form main
            nameLogin=user.getUsername();
            JOptionPane.showMessageDialog(rootPane, "Đăng nhập thành công");
            this.dispose();
            frmMain.enableMenu();
            
        } else {
            nameLogin="ADMIN";
            // Thông báo đăng nhập thất bại
            JOptionPane.showMessageDialog(rootPane,
                    "Đăng nhập thất bại");
        }
    }//GEN-LAST:event_btnDangNhapActionPerformed

