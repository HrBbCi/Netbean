    public static String Convert(int n, int b){
        List<String> lstBin = new ArrayList();
        String strSo=""; 
        int soDu = 0;
        while(n > 0){
            soDu =  n % b;
            n /= b;
            lstBin.add(""+ soDu);
        }
        
        //Duyệt theo chiều ngc lại
        for(int i = lstBin.size()-1;i>=0;i--){
            strSo += lstBin.get(i);
        }
        return strSo;
    }
