    /**
     * colTieuDe số Cột 0 số Dòng ban đầu
     */
    public void hienThiDanhSach() {
        //Khai báo tiêu đề Danh Sách
        String colTieuDe[] = new String[]{"Mã SV", "Họ Tên",
            "Điện thoại", "Địa Chỉ", "Email"};

        //Khai báo đối tượng để hiển thi lên JTable 
        DefaultTableModel model = new DefaultTableModel(colTieuDe, 0);
        List<SinhVien> lstSinhVien;
        String strHoTen = txtHoTen.getText();
        if (strHoTen.isEmpty()) {
            lstSinhVien = SinhVienBussiness.getInstance().getListStudents();
        } else {
            lstSinhVien = SinhVienBussiness.getInstance().findStudents(strHoTen);
        }

        Object objSinhVien[];
        for (SinhVien sv : lstSinhVien) {
            objSinhVien = new Object[5];
            objSinhVien[0] = sv.getMaSV();
            objSinhVien[1] = sv.getHoTen();
            objSinhVien[2] = sv.getDienThoai();
            objSinhVien[3] = sv.getDiaChi();
            objSinhVien[4] = sv.geteMail();

            //Thêm vào model
            model.addRow(objSinhVien);
        }
        //Hiển thị thông tin lên JTable 
        jTableSinhVien.setModel(model);

    }

