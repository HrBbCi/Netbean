    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int rowSelect = jTableSinhVien.getSelectedRow();
        //nếu ng dùng ko chọn sinh viên để sửa 
        if (rowSelect == -1) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn Sinh Viên xóa");
            return;
        }

        String maSV = jTableSinhVien.getValueAt(rowSelect, 0) + "";
        if (JOptionPane.showConfirmDialog(this, "Bạn muốn xóa", "Bạn chắc chắn "
                + "muốn xóa", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            boolean isSuc = SinhVienBussiness.getInstance().Xoa(maSV);
            if (isSuc) {
                hienThiDanhSach();
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

