    public void hienThiDanhSach() {
        String colTieuDe[] = new String[]{"SBD", "Họ Tên",
            "Giới Tính", "Ngày Sinh", "Quê", "Toán", "Lý", "Hóa", "Tổng"};
        DefaultTableModel model = new DefaultTableModel(colTieuDe, 0);
        List<ThiSinh> lstSinhVien;

        DateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        Object objSinhVien[];
        if (rdDiemSan.isSelected()) {
            lstSinhVien = FileAction.getInstance().findDiemSan();
        } else if (rdDiemLiet.isSelected()) {
            lstSinhVien = FileAction.getInstance().findDiemLiet();
        } else if (rdTop5.isSelected()) {
            lstSinhVien = FileAction.getInstance().findTop5();
        } else {
            lstSinhVien = FileAction.getInstance().getListStudents();
        }
        for (ThiSinh sv : lstSinhVien) {
            objSinhVien = new Object[9];
            objSinhVien[0] = sv.getSoBD();
            objSinhVien[1] = sv.getHoTen();
            objSinhVien[2] = sv.getGioiTinh();
            objSinhVien[3] = df.format(sv.getNgaySinh());
            objSinhVien[4] = sv.getQueQuan();
            objSinhVien[5] = sv.getdToan();
            objSinhVien[6] = sv.getdLy();
            objSinhVien[7] = sv.getdHoa();
            objSinhVien[8] = sv.getTongDiem();
            model.addRow(objSinhVien);
        }
        jTableList.setModel(model);
        lblTong.setText(String.valueOf(lstSinhVien.size()));
    }

