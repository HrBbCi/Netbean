    public static void hienThiDanhSachSinhVien() {
        //Khai báo 1 mảng chứa các tiêu đề danh sách 
        String tieuDe[] = new String[]{"Mã Sinh Viên",
            "Họ Tên", "Điện thoại", "Địa chỉ", "Email"};
        DefaultTableModel model = new DefaultTableModel(tieuDe, 0);

        //Lấy thông tin Danh sách Sinh Viên
        List<SinhVien> lstSinhVien = SinhVienBussiness.getInstance().layDanhSachSinhVien();

        //Duyệt từng thông tin
        Object row[];
        for (SinhVien sv : lstSinhVien) {
            row = new Object[5];
            row[0] = sv.getMaSV();
            row[1] = sv.getHoTen();
            row[2] = sv.getSoDT();
            row[3] = sv.getDiaChi();
            row[4] = sv.geteMail();
            model.addRow(row);
        }
        //Hiển thị
        jTableSinhVien.setModel(model);
    }

