    private void oppen(String title, int type, int order) {
        order=1;
        JFileChooser jfc = new JFileChooser();
        jfc.setDialogTitle(title);
        if (jfc.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            File f = jfc.getSelectedFile();
            switch (type) {
                case 1: {
                    String a = readString(f);
                    txtND.setText(a);
                    break;
                }
                case 2: {
                    order = 2;
                    int kq = JOptionPane.showConfirmDialog(rootPane, "Bạn muốn lưu ??");
                    if (kq == JOptionPane.YES_OPTION) {
                        writeString(f, txtND.getText());
                    }
                    break;
                }
                default:
                    break;
            }
        }
    }

    private static String readString(File f) {
        StringBuilder sb = new StringBuilder();
        FileReader fr = null;
        BufferedReader br = null;
        try {
            fr = new FileReader(f);

            br = new BufferedReader(fr);

            String tem = "";
            while ((tem = br.readLine()) != null) {
                //Lưu dòng vừa đọc đc vào sb
                sb.append(tem).append("\n");
            }
            fr.close();
            br.close();
        } catch (FileNotFoundException ex) {
        } catch (IOException ex) {
        }
        return sb.toString();

    }

    public void writeString(File f, String path) {
        FileWriter fw = null;
        try {
            fw = new FileWriter(f, true);

            fw.write(path);

            fw.flush();
        } catch (IOException ex) {
        } finally {
            if (fw != null) {
                try {
                    fw.close();
                } catch (IOException ex) {
                }
            }
        }
    }

