    private void btnThucHienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThucHienActionPerformed
        String strNhapDL ="";
        String ketQua ="";
        strNhapDL = txtNhapDL.getText();
        if(strNhapDL.length()==0){
            JOptionPane.showMessageDialog(rootPane, "Nhập lại đi");
            return;
        }
       
        String NhapDL[] = strNhapDL.split(",");
        int A[] = new int[NhapDL.length];
        
        for(int i = 0; i < NhapDL.length;i++){
            try {
                A[i]= Integer.parseInt(NhapDL[i]);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Nhập lại đi");
                return;
            }
            
        }
        
        Arrays.sort(A);
        String strTopBa="";
        for(int i  = A.length-1 ;i >=0;i--){
            ketQua+=""+A[i]+";";
        }
         
        strTopBa+=""+A[A.length-1]+","+A[A.length-2]+","+A[A.length-3];
        txtTopBa.setText(strTopBa);
        
        ketQua = ketQua.substring(0,ketQua.length()-1);
        txtGiamDan.setText(ketQua);
        
        String ketQuaSoLe ="";
        int tongSoLe = 0;
        for(int i  = 0 ;i < A.length;i++){
            if(A[i] %2 !=0){
                ketQuaSoLe += ""+A[i]+";";
                tongSoLe+=A[i];
            }
        }
        lblTongCacSoLe.setText("Tồng các số lẻ : "+tongSoLe);
        ketQuaSoLe = ketQuaSoLe.substring(0,ketQuaSoLe.length()-1);
        txtSoLe.setText(ketQuaSoLe);
        
        int dem[]= new int [1];
        dem[0] = strNhapDL.split("5", -1).length - 1;
        txtSoLuongSo5.setText(""+dem[0]);
        
        
        
        
    }//GEN-LAST:event_btnThucHienActionPerformed

