    private void btnDangkyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangkyActionPerformed
        String strTen = "", strTuoi="",strHonNhan="", strGioiTinh="", strKetQua="";
        int tuoi = 0;
        
        strTen  = txtTen.getText();
        strTuoi = txtTuoi.getText();
        strHonNhan = ""+cboHonNhan.getSelectedItem();
        strGioiTinh=""+cboGioiTinh.getSelectedItem();
        //Kiểm tra
        if(strTen.length()==0){
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập tên");
            return;
        }
        if(strTuoi.length()==0){
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập tuổi");
            return;
        }
        
         //Kiểm tra có phải tuổi là số không
        try {
            
            tuoi = Integer.parseInt(strTuoi);
            if(tuoi < 0){
                JOptionPane.showMessageDialog(rootPane, "Tuổi kho hợp lệ");
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập lại tuổi");
        }
        
        //Xử lý
        if(strGioiTinh.equalsIgnoreCase("Nam")){
            if(tuoi>=22){
                switch(strHonNhan){
                    case "Chưa có gia đình" : case "Ly Hôn":
                        strKetQua = "Được phép tham gia";
                        break;
                }
            }
            else
                strKetQua = "Không được phép tham gia";
        }
        if(strGioiTinh.equalsIgnoreCase("Nữ")){
            if(tuoi>=19)
                if(strHonNhan.equalsIgnoreCase("Chưa có gia đình")|| strHonNhan.equalsIgnoreCase("Ly Hôn"))
                   strKetQua = "Được phép tham gia";
        }
        if(tuoi < 19){
            strKetQua = "Không được phép tham gia";
        }
        txtKetQua.setText(strKetQua);
    }//GEN-LAST:event_btnDangkyActionPerformed

