    private void btnXemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXemActionPerformed
        btnXem.setForeground(Color.red);
        String strNhap = "", strChon = "", strSoLan = "";
        int so = 0;
        String strKetQua = "";
        strNhap = txtNhap.getText();
        strChon = "" + cboChon.getSelectedItem();

        try {
            so = Integer.parseInt(strNhap);
            if(so < 0){
                JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập lại số");
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập lại số");
            return;
        }
        int i = 0;
        do {
            if (strChon.equals("Chẵn")) {
                if (i % 2 == 0) {
                    strKetQua += i + "-";
                    strSoLan = "" + (so + 1);
                }

            } else {
                if (strChon.equals("Lẻ")) {
                    if (i % 2 != 0) {
                        strKetQua += i + "-";
                        strSoLan = "" + (so + 1);
                    }

                }
            }
            i++;

        } while (i <= so);
        strKetQua = strKetQua.substring(0, strKetQua.length() - 1);
        txtKetQua.setText(strKetQua);
        txtSoLan.setText(strSoLan);

    }//GEN-LAST:event_btnXemActionPerformed

