    private void btnThucHienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThucHienActionPerformed
        //Khai bao bien
        String strThang = "", strKetQua = "";
        int thang = 0;

        //Lấy thông tin trên giao diện đồ họa 
        strThang = txtNhapThang.getText();
        
        //Nếu ko nhập tháng 
        if (strThang.length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Bạn cần phải nhập tháng");
            //thoat ra ko thực hiện công việc phía sau
            return;
        }
        //Chuyển dổi sang dạng số
        thang = Integer.parseInt(strThang);

        //Xử lý bài toán
        switch (thang) {
            case 1:
                strKetQua = " Tháng Một";
                break;
            case 2:
                strKetQua = " Tháng Hai";
                break;
            case 3:
                strKetQua = " Tháng Ba";
                break;
            case 4:
                strKetQua = " Tháng Tư";
                break;
            case 5:
                strKetQua = " Tháng Năm";
                break;
            case 6:
                strKetQua = " Tháng Sáu";
                break;
            case 7:
                strKetQua = " Tháng Bảy";
                break;
            case 8:
                strKetQua = " Tháng Tám";
                break;
            case 9:
                strKetQua = " Tháng Chín";
                break;
            case 10:
                strKetQua = " Tháng Mười";
                break;
            case 11:
                strKetQua = " Tháng Mười Một";
                break;
            case 12:
                strKetQua = " Tháng Mười Hai";
                break;
            default: strKetQua = "Không có tháng";
            break;
        }
        //Hiển thị kết quả
        txtKetQua.setText(strKetQua);
          
    }//GEN-LAST:event_btnThucHienActionPerformed

