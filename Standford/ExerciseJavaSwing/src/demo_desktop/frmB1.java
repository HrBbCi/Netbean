    private void btnExitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExitActionPerformed
       
        int click = JOptionPane.showConfirmDialog(rootPane, "Bạn có muốn thoát không");
            if(click == JOptionPane.YES_OPTION){
                System.exit(0);
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Bạn đã hủy");
            }
    }//GEN-LAST:event_btnExitActionPerformed

    private void btnDangNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangNhapActionPerformed
        String strTDN = "", strPass = "";
        strTDN = txtTenTruyCap.getText();
        strPass = txtPass.getText();
        
        if(strTDN.length()==0){
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập tên truy cập");
            return;
        }
        
        if(strPass.length()==0){
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập mật khẩu");
            return;
        }
    
        if(strTDN.equalsIgnoreCase("stanford")&& strPass.equalsIgnoreCase("stanford.com.vn")){
            JOptionPane.showMessageDialog(rootPane, "Bạn đã đăng nhập thành công");
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Đăng nhập thất bại");
            
        }
    }//GEN-LAST:event_btnDangNhapActionPerformed

