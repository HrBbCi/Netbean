    private void btnXemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXemActionPerformed

        String strGiaTri = "", strKetQua = "", strKieu = "";
        int giaTri = 0;

        strGiaTri = txtNhap.getText();
        strKieu = cboChon.getSelectedItem().toString();
        try {
            giaTri = Integer.parseInt(strGiaTri);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Hãy nhập lại" + e.getStackTrace());
        }
       for(int i = 0 ; i<=giaTri; i++) {
            if (strKieu.equals("Chẵn")) {
                if (i % 2 == 0) {
                    strKetQua += i + "-";
                }
            } 
            else {
                if (strKieu.equals("Lẻ")) {
                    if (i % 2 != 0) {
                        strKetQua += i + "-";
                    }
                }
            }
           
        }
        //Loại bỏ ký tự trừ
        strKetQua = strKetQua.substring(0, strKetQua.length() - 1);
        txtKetQua.setText(strKetQua);
    }//GEN-LAST:event_btnXemActionPerformed

