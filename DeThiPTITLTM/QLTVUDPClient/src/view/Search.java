    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        try {
            String tenS = txtTen.getText();
            if (tenS.equals("")) {
                JOptionPane.showMessageDialog(this, "Không được bỏ trống");
            }
            ClientControl ctr = new ClientControl();
            ctr.openCon();
            ctr.sendData(tenS);
            listS = (List<Sach>) ctr.receiveData();
            ctr.closeCon();
            display(listS);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error");
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

    private void tbSachMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSachMouseClicked
        int col = tbSach.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY() / tbSach.getRowHeight();

        if (col == 5 && 0 <= row && row < listS.size()) {
            Sach s = listS.get(row);
            Edit ed = new Edit();
            ed.setS(s);
            ed.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_tbSachMouseClicked

    public void display(List<Sach> list) {
        DefaultTableModel model = (DefaultTableModel) tbSach.getModel();
        model.setRowCount(0);
        model.getDataVector().removeAllElements();
        if (list == null) {
            JOptionPane.showMessageDialog(rootPane, "NULL");
            return;
        }
        
        for (Sach s : list) {
            JButton btnEdit = new JButton("Edit");
            Vector vt = new Vector();
            vt.add(s.getId());
            vt.add(s.getTen());
            vt.add(s.getTacGia());
            vt.add(s.getNamXB());
            vt.add(s.getNhaXB());
            vt.add(btnEdit);
            model.addRow(vt);
        }
    }

