    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        ClientControl cc = new ClientControl();
        cc.openCon();
        Sach sach = new Sach();
        sach.setId(Integer.parseInt(txt1.getText()));
        sach.setTen(jTextField2.getText());
        sach.setTacGia(jTextField3.getText());
        sach.setNamXB(Integer.parseInt(jTextField4.getText()));
        sach.setNhaXB(jTextField5.getText());
        cc.sendData(sach);
        Sach s = (Sach) cc.receiveData();
        cc.closeCon();
        if (s != null) {
            JOptionPane.showMessageDialog(rootPane, "Thành công");
            Search ss = new Search();
            ss.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Kiểm tra lại");
        }
        cc.closeCon();
    }//GEN-LAST:event_btnEditActionPerformed

