    private void btnTKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTKActionPerformed
        try {
            String tenS = txtTenSach.getText();
            if (tenS.equals("")) {
                JOptionPane.showMessageDialog(this, "Không được bỏ trống");
            }
            ClientControl ctr = new ClientControl();
            ctr.openConnection();
            ctr.sendData(tenS);
            listS = (List<Sach>) ctr.receiveData();
            ctr.closeConnection();
            displayList(listS);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error");
        }
    }//GEN-LAST:event_btnTKActionPerformed

    private void tbSachMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSachMouseClicked
        int col = tbSach.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY() / tbSach.getRowHeight();
        if (col == 5 && 0 <= row && row < listS.size()) {
            Sach s = listS.get(row);
            EditSach es = new EditSach();
            es.setS(s);
            es.setVisible(true);
            this.dispose();
//            JOptionPane.showMessageDialog(this, khachHang.getTenKhachHang());
        }
    }//GEN-LAST:event_tbSachMouseClicked

    public void displayList(List<Sach> listSach) {
        DefaultTableModel model = (DefaultTableModel) tbSach.getModel();
        model.setRowCount(0);
        model.getDataVector().removeAllElements();
        if (listSach == null) {
            JOptionPane.showMessageDialog(this, "Không có Sách nào!");
            return;
        }
        for (Sach i : listSach) {
            JButton btnSlt = new JButton("Edit");
            Vector tmp = new Vector();
            tmp.add(i.getId());
            tmp.add(i.getTen());
            tmp.add(i.getTacGia());
            tmp.add(i.getNamXB());
            tmp.add(i.getNhaXB());
            tmp.add(btnSlt);
            model.addRow(tmp);
        }
    }

