    public void editS() {
        String sid = txtID.getText();
        int id = Integer.parseInt(sid);
        
        String ten = txtTen.getText();
        String tg = txtTG.getText();
        String snam = txtNamXB.getText();
        int nam = Integer.parseInt(snam);
        String nxb = txtNhaXB.getText();
        Sach k = new Sach();
        k.setId(id);
        k.setTen(ten);
        k.setTacGia(tg);
        k.setNamXB(nam);
        k.setNhaXB(nxb);
        ClientControl cc = new ClientControl();
        cc.openConnection();
        cc.sendData(k);
        Sach s = (Sach) cc.receiveData();
        cc.closeConnection();
        if (s != null) {
            JOptionPane.showMessageDialog(rootPane, "Thành công");
            SearchSach ss = new SearchSach();
            ss.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Kiểm tra lại");
        }
        
    }

