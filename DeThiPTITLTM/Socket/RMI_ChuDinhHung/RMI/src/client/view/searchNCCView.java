    private void tblNCCMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNCCMouseClicked
        int col = tblNCC.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY() / tblNCC.getRowHeight();
        if (col == 4 && 0 <= row && row < listNCC.length) {
            NCC ncc = listNCC[row];
            new NhapHangView(ncc).setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_tblNCCMouseClicked

