    private  void showTable() {
        DefaultTableModel model = (DefaultTableModel) tblNCC.getModel();
        model.setRowCount(0);
        model.getDataVector().removeAllElements();
        if (listNCC == null) {
            JOptionPane.showMessageDialog(this, "Không có NCC nào!");
            return;
        }
        for (NCC i : listNCC) {
            JButton btnSlt = new JButton("Select");
            Vector tmp = new Vector();
            tmp.add(i.getId());
            tmp.add(i.getName());
            tmp.add(i.getAddress());
            tmp.add(i.getPhone());
            tmp.add(btnSlt);
            model.addRow(tmp);
        }
    }
