    public void showResult(Schedule[] listSchedule)
    {
        this.listSchedule = listSchedule;
        result_tb.getDataVector().removeAllElements();
        for(int i = 0; i < listSchedule.length; i++)
        {
            result_tb.addRow(new Object[]{listSchedule[i].getId(), listSchedule[i].getFilm().getName(),listSchedule[i].getRoom().getName(),listSchedule[i].getTime(),listSchedule[i].getDate(), listSchedule[i].getPrice()});
        } 
    }
