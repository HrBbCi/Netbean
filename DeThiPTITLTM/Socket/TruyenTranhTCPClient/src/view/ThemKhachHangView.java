    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String tenKhachHang = txtTenKhachHang.getText();
            String soDienThoai = txtSoDienThoai.getText();
            String diaChi = txtDiaChi.getText();
            String eMail = txtEmail.getText();
            String moTa = areaMoTa.getText();
            KhachHang khachHang = new KhachHang(0, tenKhachHang, soDienThoai, diaChi, eMail, moTa);
            ClientCtr ctr= new  ClientCtr();
            ctr.openConnection();
            ctr.sendData(khachHang);
            String result = (String) ctr.receiveData();
            ctr.closeConnection();
            JOptionPane.showMessageDialog(this, result);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this," Nhập sai định dang!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

