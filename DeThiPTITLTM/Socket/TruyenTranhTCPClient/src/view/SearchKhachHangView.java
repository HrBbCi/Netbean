    private void showTableKhachHang() {
        DefaultTableModel model = (DefaultTableModel) tblKhachHang.getModel();
        model.getDataVector().removeAllElements();
        if (listKhachHangs == null) {
            JOptionPane.showMessageDialog(this, "Không có khách hàng nào!");
            return;
        }
        for (KhachHang i : listKhachHangs) {
            JButton btnSlt = new JButton("Select");
            Vector tmp = new Vector();
            tmp.add(i.getMaKhachHang());
            tmp.add(i.getTenKhachHang());
            tmp.add(i.getSoDienThoai());
            tmp.add(i.getDiaChi());
            tmp.add(i.getEmail());
            tmp.add(btnSlt);
            model.addRow(tmp);
        }
    }
    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed

        try {
            String tenKhachHang = txtTenKhachHang.getText();
            if(tenKhachHang.equals("")) {
                JOptionPane.showMessageDialog(this, "Không được bỏ trống");
            }
            ClientCtr ctr = new ClientCtr();
            ctr.openConnection();
            ctr.sendData(tenKhachHang);
            listKhachHangs = (KhachHang[]) ctr.receiveData();
            ctr.closeConnection();
            showTableKhachHang();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error");
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void tblKhachHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKhachHangMouseClicked
        int col = tblKhachHang.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY() / tblKhachHang.getRowHeight();
        if (col == 5 && 0 <= row && row < listKhachHangs.length) {
            KhachHang khachHang = listKhachHangs[row];
            new MuonSachView(khachHang).setVisible(true);
            this.setVisible(false);
//            JOptionPane.showMessageDialog(this, khachHang.getTenKhachHang());
        }
    }//GEN-LAST:event_tblKhachHangMouseClicked

