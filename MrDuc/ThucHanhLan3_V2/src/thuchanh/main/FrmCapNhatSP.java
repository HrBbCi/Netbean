  private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
    Product pd = new Product();
    ArrayList<Product> lst = pdio.readAll();
    String strID = txtID.getText();
    String strName = txtName.getText();
    String strPrice = txtprice.getText();
    if (strID.isEmpty()) {
      lblID.setText("ID trống");
      return;
    }
    lblID.setText("");
    if (strName.isEmpty()) {
      lblName.setText("Tên trống");
      return;
    }
    
    lblName.setText("");
    pd.setId(strID);
    pd.setName(strName);
    
    String pd1 = (String) cboType.getSelectedItem();
    ProductType pt = new ProductType();
    pt.setId(pd1);
    pd.setPd(pt);
    
    if (!strPrice.isEmpty()) {
      int price = 0;
      try {
        price = Integer.parseInt(strPrice);
      } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(rootPane, "Lỗi");
        return;
      }
    }
    pd.setPrice(txtprice.getText());
    boolean kq = true;
    if (maSP.isEmpty() || maSP == null) {
      for (int i = 0; i < lst.size(); i++) {
        if (lst.get(i).getId().equals(strID)) {
          lblID.setText("ID đã tồn tại");
          kq = false;
          return;
        }
      }
      if (kq) {
        pdio.them(pd);
      }
    } else {
      pdio.sua(pd);
    }
    pdio.write(pd);
    new FrmTimKiemSP().setVisible(true);
    this.dispose();

  }//GEN-LAST:event_btnOKActionPerformed

  private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
    if (!maSP.isEmpty()) {
      this.setTitle("Edit");
      btnOK.setText("Edit");
      displayDetails();
    } else {
      this.setTitle("Add");
      btnOK.setText("Add");
    } 
    displayProduct();
  }//GEN-LAST:event_formWindowOpened
  
  private void displayDetails() {
    Product pd = pdio.search(maSP);
    if (pd != null) {
      txtID.setText(pd.getId());
      txtName.setText(pd.getName());
      txtprice.setText(pd.getPrice());
      cboType.setSelectedItem(pd.getPd().getId());
    } else {
      System.out.println("rong");
    }
  }

  private void displayProduct() {
    ArrayList<ProductType> lst = new ProductTypeIO().readAll();
    cboType.removeAllItems();
    for (ProductType pt : lst) {
      cboType.addItem(pt.getId());
    }
  }

