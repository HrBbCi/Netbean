  private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
      Manage mn = new Manage();
      Product pd = new Product();
      //doc danh sach
      ArrayList<Product> lsp = new ArrayList<>();
      mn.docFile();
      IOFile.inFile(lsp, "BT.dat");
      //ket thuc doc danh sach

      //Lay cac gia tri cua product
      String strID = txtID.getText();
      String strName = txtName.getText();
      String strPrice = txtprice.getText();
      if (strID.isEmpty()) {
          lblID.setText("ID trống");
          return;
      }
      lblID.setText("");
      if (strName.isEmpty()) {
          lblName.setText("Tên trống");
          return;
      }

      lblName.setText("");
      pd.setId(strID);
      pd.setName(strName);
      String idType = (String) cboType.getSelectedItem();
      ProductType pt = new ProductType();
      pt.setId(idType);
      pd.setPd(pt);

      if (!strPrice.isEmpty()) {
          int price = 0;
          try {
              price = Integer.parseInt(strPrice);
          } catch (NumberFormatException e) {
              JOptionPane.showMessageDialog(rootPane, "Lỗi");
              return;
          }
      }
      pd.setPrice(txtprice.getText());
      //Xong lay gia tri

      //Kiem tra la them moi hay sua
      boolean kq = true;

      for (int i = 0; i < lsp.size(); i++) {
          if (lsp.get(i).getId().equals(strID)) {
              lblID.setText("ID đã tồn tại");
              kq = false;
              return;
          }
      }
      if (kq) {
          mn.them(pd);
          mn.ghiFile();
      }

      //hien thi form danh sach len 
      new FrmTimKiemSP().setVisible(true);
      this.dispose();

  }//GEN-LAST:event_btnOKActionPerformed

