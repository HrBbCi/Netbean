  private void search(){
    String id = txtID.getText();
    String name = txtName.getText();
    List<Lecturer> lst = new ArrayList<>();
    if (name != null && (id.isEmpty() || id == null)) {
      lst = LecturerDAO.getInstance().find("", name);
    } else if (id != null && (name.isEmpty() || name == null)) {
      lst = LecturerDAO.getInstance().find(id, "");
    } else {
      lst = LecturerDAO.getInstance().find(id, name);
    }
    display(lst);
  }
  public void display() {
    display(LecturerDAO.getInstance().getAll());
  }

  public void display(List<Lecturer> lst) {
    String colTieuDe[] = new String[]{"ID", "Name", "Sex",
      "Birthday", "Adress", "CounselorCLass"};
    DefaultTableModel model = new DefaultTableModel(colTieuDe, 0);
    Object oL[];
    DateFormat df = new SimpleDateFormat("dd/MM/yyyy");
    for (Lecturer lt : lst) {
      oL = new Object[colTieuDe.length];
      oL[0] = lt.getIdLecturer();
      oL[1] = lt.getName();
      if (lt.getSex() == 1) {
        oL[2] = "Male";
      } else {
        oL[2] = "Female";
      }
      if (lt.getBirthday() != null) {
        oL[3] = df.format(lt.getBirthday());
      }
      oL[4] = lt.getAdress();
      oL[5] = lt.getCounselorClass();
      model.addRow(oL);
    }
    TableRowSorter sorter = new TableRowSorter(model);
    tbLecturer.setRowSorter(sorter);
    tbLecturer.setModel(model);
  }

