  private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
    Student s = StudentDAO.getInstance().getByID(idStudent);
    lblID.setText(s.getIdStudent());
    lblName.setText(s.getName());
    if (s.getSex() == 0) {
      lblSex.setText("Female");
    } else {
      lblSex.setText("Male");
    }
    lblBirt.setText(s.getBirthday() + "");
    lblAdress.setText(s.getAdress());
    lblMobiel.setText(s.getMobile());
    lblEmail.setText(s.getEmail());
    lblCalss.setText(s.getCs().getName());
  }//GEN-LAST:event_formWindowOpened

