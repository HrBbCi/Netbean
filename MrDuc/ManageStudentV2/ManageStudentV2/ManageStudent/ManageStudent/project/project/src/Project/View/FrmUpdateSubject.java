  private void Update() {
    Subjects sb = new Subjects();
    if (txtID.getText().isEmpty()) {
      Jop("Please fill ID");
      return;
    }
    if (txtName.getText().isEmpty()) {
      Jop("Please fill name");
      return;
    }
    String strCr = txtCredit.getText();
    int cre = 0;
    try {
      cre = Integer.parseInt(strCr);
    } catch (NumberFormatException e) {
      Jop("Error Number");
      return;
    }
    sb.setIdSubject(txtID.getText());
    sb.setNameSubject(txtName.getText());
    sb.setCredit(cre);
    boolean kq = false;
    if (idS == null || idS.isEmpty()) {
      if (SubjectsDAO.getInstance().isTest(sb.getIdSubject())) {
        kq = svd.newAdd(sb);
      } else {
        Jop("ID existed ");
        return;
      }
    } else {
      kq = svd.edit(sb);
    }
    if (kq) {
      JOptionPane.showMessageDialog(rootPane, "Sucess");
      frmd.display();
      frmd.setVisible(true);
    }
    this.dispose();
  }

  private void displayDetails() {
    Subjects sb1 = svd.getByID(idS);
    if (sb1 != null) {
      txtID.setText(sb1.getIdSubject());
      txtID.setEnabled(false);
      txtName.setText(sb1.getNameSubject());
      txtCredit.setText(sb1.getCredit() + "");
    } else {
      System.out.println("sb null");
    }
  }

