  private void Update() {
    Systems us = new Systems();
    if (txtusername.getText().isEmpty() || txtusername.getText().equals("")) {
      JOptionPane.showMessageDialog(rootPane, "Please fill username");
      return;
    }
    char[] arr = txtPassword.getPassword();
    String password = new String(arr);
    us.setUsername(txtusername.getText());
    us.setPassword(password);
    boolean kq = false;
    if (ID == null || ID.isEmpty()) {
      if (SystemsDAO.getInstance().check(txtusername.getText())) {
        kq = usb.newAdd(us);
      } else {
        JOptionPane.showMessageDialog(rootPane, "Username existed");
        return;
      }
    } else {
      kq = usb.edit(us);
    }
    if (kq) {
      JOptionPane.showMessageDialog(rootPane, "Sucess");
      frmM.displayUsers();
      frmM.setVisible(true);
    }
    this.dispose();
  }

  private void hienThiChiTiet() {
    Systems us = usb.getByID(ID);
    if (us != null) {
      txtusername.setText(us.getUsername());
      txtusername.setEnabled(false);
      txtPassword.setText(us.getPassword());
    } else {
      System.out.println("sv rong");
    }
  }

