  public static void displayUsers() {
    SystemsDAO usb = new SystemsDAO();
    displayUsers(usb.getAll());
  }

  public static void displayUsers(List<Systems> lstU) {
    MD5 md5 = new MD5();
    String colTieuDe[] = new String[]{"Username", "Password"};
    DefaultTableModel model = new DefaultTableModel(colTieuDe, 0);
    Object objUs[];
    for (Systems us : lstU) {
      objUs = new Object[colTieuDe.length];
      objUs[0] = us.getUsername();
      try {
        objUs[1] = md5.testMD5(us.getPassword());
      } catch (Exception ex) {
        System.out.println("Error convert MD5");
      }
      model.addRow(objUs);
    }
    TableRowSorter tbs = new TableRowSorter(model);
    tbUsers.setRowSorter(tbs);
    tbUsers.setModel(model);

  }

