  private void Update() {
    Student sv = new Student();
    if (txtMaSV.getText().isEmpty()) {
      Jop("Please fill ID");
      return;
    }
    if (txtHoTen.getText().isEmpty()) {
      Jop("Please fill name");
      return;
    }
    if (!rbtnNam.isSelected() && !rbtnNu.isSelected()) {
      Jop("Please fill sex");
      return;
    }

    if (svd.checkSDT(txtDienThoai.getText()) == false) {
      Jop("Please fill mobilephone");
      return;
    }

    sv.setIdStudent(txtMaSV.getText());
    sv.setName(txtHoTen.getText());
    if (rbtnNam.isSelected()) {
      sv.setSex(1);
    } else {
      sv.setSex(0);
    }
    sv.setBirthday(dateNgaySinh.getDate());
    sv.setAdress(txtDiaChi.getText());
    sv.setEmail(txtEmail.getText());
    sv.setMobile(txtDienThoai.getText());

    String pd1 = (String) cboClass.getSelectedItem();
    Classs cs = new Classs();
    cs.setName(pd1);
    sv.setCs(cs);
    
    boolean kq = false;
    if (maSV == null || maSV.isEmpty()) {
      if (StudentDAO.getInstance().isTest(sv.getIdStudent())) {
        kq = svd.newAdd(sv);
      } else {
        Jop("ID existed");
        return;
      }
    } else {
      kq = svd.edit(sv);
    }
    if (kq) {
      JOptionPane.showMessageDialog(rootPane, "Sucess");
      frmd.hienThiDanhSachSV();
      frmd.setVisible(true);
    }
    this.dispose();
  }

  private void displayClass() {
    List<Classs> lst = new ClassDAO().getAllCLass();
    cboClass.removeAllItems();
    lst.forEach((ck) -> {
      cboClass.addItem(ck.getName());
    });
  }

  private void displayDetails() {
    Student sv1 = svd.getByID(maSV);
    if (sv1 != null) {
      cboClass.setSelectedItem(cboMa);
      txtMaSV.setText(sv1.getIdStudent());
      txtMaSV.setEnabled(false);
      txtHoTen.setText(sv1.getName());
      txtDiaChi.setText(sv1.getAdress());
      if (sv1.getSex() == 1) {
        rbtnNam.setSelected(true);
      } else {
        rbtnNu.setSelected(true);
      }
      dateNgaySinh.setDate(sv1.getBirthday());
      txtDienThoai.setText(sv1.getMobile());
      txtEmail.setText(sv1.getEmail());
      
    } else {
      System.out.println("sv rong");
    }
  }

