  private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
    int select = tbSubject.getSelectedRow();
    if (select == -1) {
      JOptionPane.showMessageDialog(this,
              "Please choose subject");
      return;
    }
    String id = (String) tbSubject.getValueAt(select, 0);
    FrmUpdateSubject frm = new FrmUpdateSubject();
    frm.setIdS(id);
    frm.setVisible(true);
  }//GEN-LAST:event_btnEditActionPerformed

  private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
    int select = tbSubject.getSelectedRow();
    if (select == -1) {
      JOptionPane.showMessageDialog(this,
              "Please choose Subject");
      return;
    }
    SubjectsDAO svd = new SubjectsDAO();
    String id = (String) tbSubject.getValueAt(select, 0);
    if (JOptionPane.showConfirmDialog(this, "Do you want to delete", "Are you sure to delete "
            + "", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
      boolean kq = svd.delete(id);
      if (kq) {
        JOptionPane.showMessageDialog(rootPane, "Sucess");
        display();
      }
    }
  }//GEN-LAST:event_btnDelActionPerformed

