  private void search() {
    String name = txtname.getText();
    String id = txtID.getText();
    List<Subjects> lst = new ArrayList<>();
    if (name != null && (id.isEmpty() || id == null)) {
      lst = SubjectsDAO.getInstance().find("", name);
    } else if (id != null && (name.isEmpty() || name == null)) {
      lst = SubjectsDAO.getInstance().find(id, "");
    } else {
      lst = SubjectsDAO.getInstance().find(id, name);
    }
    displayList(lst);
  }

  public void display() {
    displayList(SubjectsDAO.getInstance().getAll());
  }

  public void displayList(List<Subjects> lstSub) {
    String colTieuDe[] = new String[]{"ID", "Name", "Credit"};
    DefaultTableModel model = new DefaultTableModel(colTieuDe, 0);
    Object objSub[];
    for (Subjects sb : lstSub) {
      objSub = new Object[colTieuDe.length];
      objSub[0] = sb.getIdSubject();
      objSub[1] = sb.getNameSubject();
      objSub[2] = sb.getCredit();
      model.addRow(objSub);
    }
    tbSubject.setModel(model);
  }

