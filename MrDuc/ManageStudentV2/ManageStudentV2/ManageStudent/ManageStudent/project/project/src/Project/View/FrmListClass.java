  public void display() {
    display(ClassDAO.getInstance().getAll());
  }

  public void display(List<Classs> lstLop) {
    String colTieuDe[] = new String[]{"Name Class", "Amount student"};
    DefaultTableModel model = new DefaultTableModel(colTieuDe, 0);
    Object oLop[];
    for (Classs lp : lstLop) {
      oLop = new Object[colTieuDe.length];
      oLop[0] = lp.getName();
      oLop[1] = lp.getAmount();
      model.addRow(oLop);
    }
    tbLop.setModel(model);
  }

  public void search() {
    String name = txtTenLop.getText();
    List<Classs> lst = new ArrayList<>();
    if (name != null) {
      lst = ClassDAO.getInstance().find(name);
    } else {
      lst = ClassDAO.getInstance().find("");
    }
    display(lst);
  }

