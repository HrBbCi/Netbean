  private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
    int rowSelect = tbDS.getSelectedRow();
    if (rowSelect < 0) {
      JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn sản phẩm sửa");
      return;
    }
    String id = tbDS.getValueAt(rowSelect, 0) + "";
    FrmCapNhatSP frm = new FrmCapNhatSP();
    frm.setMaSP(id);
    frm.setVisible(true);
  }//GEN-LAST:event_btnSuaActionPerformed

  private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
    int select = tbDS.getSelectedRow();
    if (select == -1) {
      JOptionPane.showMessageDialog(this,
              "Please choose Subject");
      return;
    }
    Manage mn = new Manage();
    mn.docFile();
    String id = (String) tbDS.getValueAt(select, 0);
    ArrayList<Product> lst = new ArrayList<>();
    if (JOptionPane.showConfirmDialog(this, "Do you want to delete", "Are you sure to delete "
            + "", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
      boolean kq = mn.xoa(id);
      IOFile.inFile(lst, "BT.dat");
      if (kq) {
        JOptionPane.showMessageDialog(rootPane, "Sucess");
        hienThi(lst);
      }
    }
  }//GEN-LAST:event_btnXoaActionPerformed

  public void hienThi(List<Product> lst) {
    String col[] = {"ID", "Name", "Type", "Price"};
    DefaultTableModel model = new DefaultTableModel(col, 0);
    Object obj[] = null;
    for (Product pd : lst) {
      obj = new Object[col.length];
      obj[0] = pd.getId();
      obj[1] = pd.getName();
      obj[2] = pd.getPd().getId();
      obj[3] = pd.getPrice();
      model.addRow(obj);
    }
    tbDS.setModel(model);
    tbDS.setAutoCreateRowSorter(true);
  }

