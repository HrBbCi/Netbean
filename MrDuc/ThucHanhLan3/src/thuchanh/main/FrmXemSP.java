  private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
    ArrayList<Product> lst = pdio.readAll();
    String strID = txtID.getText();
    Product pd = new Product();
    if (strID == null || strID.isEmpty()) {
      txtID.setText(lst.get(0).getId());
      txtName.setText(lst.get(0).getName());
      txtprice.setText(lst.get(0).getPrice());
      txttype.setText(lst.get(0).getType());
    } else {
      for (int i = 0; i < lst.size(); i++) {
        if (lst.get(i).getId().equals(strID)) {
          if (i + 1 < lst.size()) {
            pd = lst.get(i + 1);
          } else {
            int a = i + 1 - lst.size();
            pd = lst.get(a);
          }
          txtID.setText(pd.getId());
          txtName.setText(pd.getName());
          txtprice.setText(pd.getPrice());
          txttype.setText(pd.getType());
        }
      }
    }
  }//GEN-LAST:event_btnNextActionPerformed

  private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
    ArrayList<Product> lst = pdio.readAll();
    String strID = txtID.getText();
    String strName = txtName.getText();
    Product pd = new Product();
    if (strID == null || strID.isEmpty()) {
      txtID.setText(lst.get(0).getId());
      txtName.setText(lst.get(0).getName());
      txtprice.setText(lst.get(0).getPrice());
      txttype.setText(lst.get(0).getType());
    } else {
      for (int i = 0; i < lst.size(); i++) {
        if (lst.get(i).getId().equals(strID)) {
          if (i > 0) {
            pd = lst.get(i - 1);
          } else {
            pd = lst.get(lst.size() - 1);
          }
          txtID.setText(pd.getId());
          txtName.setText(pd.getName());
          txtprice.setText(pd.getPrice());
          txttype.setText(pd.getType());
        }
      }
    }
  }//GEN-LAST:event_btnBackActionPerformed

