  Randomm rd;
  public long start, end;
  public long start1, end1;

  public class Xe1 extends Thread {

    @Override
    public void run() {
      reset();
      rd = new Randomm();
      start = System.currentTimeMillis();
      for (soXe1 = 10; soXe1 >= 0; soXe1--) {
        lblDangCHo1.setText("Xe đang chạy");
        try {
          Thread.sleep(rd.random());
        } catch (InterruptedException ex) {
        }
        SwingUtilities.invokeLater(new Runnable() {
          @Override
          public void run() {
            lblXe1Giay.setText(soXe1 + "");
          }
        });

        if (soXe1 == 0) {
          end = System.currentTimeMillis();
          lblDangCHo1.setText("Về Đích " + ((end - start) / 1000L) + "s");
          if (soXe2 != 0) {
            lblKetQua.setText("Xe 1 thắng cuộc");
          }
          break;
        }
      }

    }

  }

